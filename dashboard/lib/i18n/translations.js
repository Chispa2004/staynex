export const translatePhrase = (language, value, replacements = {}) => {
  if (value === null || value === undefined) return value;
  const source = String(value);
  const replace = (text) => Object.entries(replacements)
    .reduce((current, [key, replacement]) => current.replaceAll(`{${key}}`, replacement), text);
  const direct = phraseTranslations[language]?.[source] || phraseTranslations.en?.[source];

  if (direct) return replace(direct);

  const pattern = (phrasePatterns[language] || []).find(([matcher]) => matcher.test(source));
  if (pattern) {
    return source.replace(pattern[0], pattern[1]);
  }

  if (language !== DEFAULT_DASHBOARD_LANGUAGE && process.env.NODE_ENV !== 'production') {
    console.warn('Missing dashboard phrase translation', { language, source });
  }

  return replace(source);
};


